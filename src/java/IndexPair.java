public class IndexPair {
    private int first = 0;
    private int second = 0;

    public IndexPair(int index, int index2) {
        this.first = index;
        this.second = index2;
    }

    public IndexPair() {
    }

    public int getFirst() {
        return first;
    }

    public int getSecond() {
        return second;
    }
}
